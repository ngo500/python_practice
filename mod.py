"""
Module file with functions for squaring numbers, multiplying numbers by 2, and summing 2 numbers
"""

def square(num):
    """
    Takes a number as input, and returns the square of that number
    """
    return num ** 2

def double(num):
    """
    Takes a number as input, and returns that number multiplied by 2
    """
    return num * 2

def add(x, y):
    """
    Takes 2 numbers, and returns their sum
    """
    return x + y
